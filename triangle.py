def area(a, h):
    '''Принимает сторону и проведённую к этой стороне высоту трекгольника - числа a и h, возвращает площадь этого треугольника'''
    return a * h / 2
def perimeter(a, b, c):
    '''Принимает стороны треугольника - числа a, b и c, возвращает периметр этого треугольника'''
    return a + b + c